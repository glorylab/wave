import 'dart:async';
import 'dart:math';

import 'package:flutter/widgets.dart';
import 'package:wave/config.dart';

class WaveWidget extends StatefulWidget {
  final Config config;
  final Size size;
  final double waveAmplitude;
  final double wavePhase;
  final double waveFrequency;
  final double heightPercentage;
  final int? duration;
  final Color? backgroundColor;
  final DecorationImage? backgroundImage;
  final bool isLoop;
  final Widget? child;

  WaveWidget({
    required this.config,
    required this.size,
    this.waveAmplitude = 20.0,
    this.wavePhase = 10.0,
    this.waveFrequency = 1.6,
    this.heightPercentage = 0.2,
    this.duration = 6000,
    this.backgroundColor,
    this.backgroundImage,
    this.isLoop = true,
    this.child,
  });

  @override
  State<StatefulWidget> createState() => _WaveWidgetState();
}

class _WaveWidgetState extends State<WaveWidget> with TickerProviderStateMixin {
  late List<AnimationController> _waveControllers;
  late List<Animation<double>> _wavePhaseValues;

  List<double> _waveAmplitudes = [];
  Map<Animation<double>, AnimationController>? valueList;
  Timer? _endAnimationTimer;

  void _initAnimations() {
    if (widget.config.colorMode == ColorMode.custom) {
      _waveControllers =
          (widget.config as CustomConfig).durations!.map((duration) {
        _waveAmplitudes.add(widget.waveAmplitude + 10);
        return AnimationController(
            vsync: this, duration: Duration(milliseconds: duration));
      }).toList();

      _wavePhaseValues = _waveControllers.map((controller) {
        CurvedAnimation _curve =
            CurvedAnimation(parent: controller, curve: Curves.easeInOut);

        Animation<double> value = Tween(
          begin: widget.wavePhase,
          end: 360 + widget.wavePhase,
        ).animate(
          _curve,
        );

        value.addStatusListener((status) {
          switch (status) {
            case AnimationStatus.completed:
              controller.reverse();
              break;
            case AnimationStatus.dismissed:
              controller.forward();
              break;
            default:
              break;
          }
        });
        controller.forward();
        return value;
      }).toList();

      // If isLoop is false, stop the animation after the specified duration.
      if (!widget.isLoop) {
        _endAnimationTimer =
            Timer(Duration(milliseconds: widget.duration!), () {
          for (AnimationController waveController in _waveControllers) {
            waveController.stop();
          }
        });
      }
    }
  }

  List<Widget> _buildPaints() {
    List<Widget> paints = [];
    if (widget.config.colorMode == ColorMode.custom) {
      final cconfig = widget.config as CustomConfig;

      List<Color>? _colors = cconfig.colors;
      List<List<Color>>? _gradients = cconfig.gradients;
      Alignment? begin = cconfig.gradientBegin;
      Alignment? end = cconfig.gradientEnd;

      for (int i = 0; i < _wavePhaseValues.length; i++) {
        paints.add(
          CustomPaint(
            painter: _CustomWavePainter(
              colorMode: widget.config.colorMode,
              color: _colors == null ? null : _colors[i],
              gradient: _gradients == null ? null : _gradients[i],
              gradientBegin: begin,
              gradientEnd: end,
              heightPercentage: cconfig.heightPercentages![i],
              repaint: _waveControllers[i],
              waveFrequency: widget.waveFrequency,
              wavePhaseValue: _wavePhaseValues[i],
              waveAmplitude: _waveAmplitudes[i],
              blur: cconfig.blur,
            ),
            child: widget.child,
            size: widget.size,
          ),
        );
      }
    }
    return paints;
  }

  void _disposeAnimations() {
    _waveControllers.forEach((controller) {
      controller.dispose();
    });
  }

  @override
  void initState() {
    super.initState();
    _initAnimations();
  }

  @override
  void dispose() {
    _disposeAnimations();
    _endAnimationTimer?.cancel();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: BoxDecoration(
        color: widget.backgroundColor,
        image: widget.backgroundImage,
      ),
      child: Stack(
        children: _buildPaints(),
      ),
    );
  }
}

/// Meta data of layer
class Layer {
  final Color? color;
  final List<Color>? gradient;
  final MaskFilter? blur;
  final Path? path;
  final double? amplitude;
  final double? phase;

  Layer({
    this.color,
    this.gradient,
    this.blur,
    this.path,
    this.amplitude,
    this.phase,
  });
}

class _CustomWavePainter extends CustomPainter {
  final ColorMode? colorMode;
  final Color? color;
  final List<Color>? gradient;
  final Alignment? gradientBegin;
  final Alignment? gradientEnd;
  final MaskFilter? blur;

  double? waveAmplitude;

  Animation<double>? wavePhaseValue;

  double? waveFrequency;

  double? heightPercentage;

  double _tempA = 0.0;
  double _tempB = 0.0;
  double viewWidth = 0.0;
  Paint _paint = Paint();

  _CustomWavePainter({
    this.colorMode,
    this.color,
    this.gradient,
    this.gradientBegin,
    this.gradientEnd,
    this.blur,
    this.heightPercentage,
    this.waveFrequency,
    this.wavePhaseValue,
    this.waveAmplitude,
    required Listenable repaint,
  }) : super(repaint: repaint);

  void _setPaths(double viewCenterY, Size size, Canvas canvas) {
    Layer _layer = Layer(
      path: Path(),
      color: color,
      gradient: gradient,
      blur: blur,
      amplitude: (-1.6 + 0.8) * waveAmplitude!,
      phase: wavePhaseValue!.value * 2 + 30,
    );

    _layer.path!.reset();
    _layer.path!.moveTo(
        0.0,
        viewCenterY +
            _layer.amplitude! * _getSinY(_layer.phase!, waveFrequency!, -1));
    for (int i = 1; i < size.width + 1; i++) {
      _layer.path!.lineTo(
          i.toDouble(),
          viewCenterY +
              _layer.amplitude! * _getSinY(_layer.phase!, waveFrequency!, i));
    }

    _layer.path!.lineTo(size.width, size.height);
    _layer.path!.lineTo(0.0, size.height);
    _layer.path!.close();
    if (_layer.color != null) {
      _paint.color = _layer.color!;
    }
    if (_layer.gradient != null) {
      var rect = Offset.zero &
          Size(size.width, size.height - viewCenterY * heightPercentage!);
      _paint.shader = LinearGradient(
              begin: gradientBegin == null
                  ? Alignment.bottomCenter
                  : gradientBegin!,
              end: gradientEnd == null ? Alignment.topCenter : gradientEnd!,
              colors: _layer.gradient!)
          .createShader(rect);
    }
    if (_layer.blur != null) {
      _paint.maskFilter = _layer.blur;
    }

    _paint.style = PaintingStyle.fill;
    canvas.drawPath(_layer.path!, _paint);
  }

  @override
  void paint(Canvas canvas, Size size) {
    double viewCenterY = size.height * (heightPercentage! + 0.1);
    viewWidth = size.width;

    _setPaths(viewCenterY, size, canvas);
  }

  @override
  bool shouldRepaint(CustomPainter oldDelegate) => false;

  double _getSinY(
    double startradius,
    double waveFrequency,
    int currentposition,
  ) {
    if (_tempA == 0) {
      _tempA = pi / viewWidth;
    }
    if (_tempB == 0) {
      _tempB = 2 * pi / 360.0;
    }

    return sin(
      _tempA * waveFrequency * (currentposition + 1) + startradius * _tempB,
    );
  }
}
